export default function converter(currentTime){
    if (currentTime == 1){return "måndag"};
    if (currentTime == 2){return "tisdag"};
    if (currentTime == 3){return "onsdag"};
    if (currentTime == 4){return "torsdag"};
    if (currentTime == 5){return "fredag"};
    if (currentTime == 6){return "lördag"};
    if (currentTime == 7){return "söndag"};
}